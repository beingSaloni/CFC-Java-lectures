package com.company.assignment1;

public class Sevena {
    public static void main(String[] args) {
        int co = 5 ;

        for (int i = 0; i < co; i++) {
            for (int j = 0; j <= i ; j++) {
                System.out.print("* ");

            }
            System.out.println("");

        }

    int n = 5 ;
    int row = 0 ;
    while (row < n){
        int col = 0 ;
        while (col<= row){
            System.out.print("* ");
            col++ ;
        }
        System.out.println();
        row++ ;
    }}


}
